print("Welcome! This program will help you calculate the area of your room.")
width_of_room = float(input("Enter the width of your room in square meters:"))
length_of_room = float(input("Enter the length of your room in square meters:"))
calculate_area_of_room = width_of_room * length_of_room 
print(f"The area of your room is {calculate_area_of_room}")
